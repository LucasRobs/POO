public abstract class Horario{
  private int hora;
  private int minuto;

  public int getHora() {
      return hora;
  }

  public void setHora(int hora) {
      this.hora = hora;
  }

  public int getMinuto() {
      return minuto;
  }

  public void setMinuto(int minuto) {
      this.minuto = minuto;
  }
}
